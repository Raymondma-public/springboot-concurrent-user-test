package com.marcnuri.demo.springmockmvc;

import org.springframework.http.HttpStatus;

/**
 * Created by Marc Nuri <marc@marcnuri.com> on 2018-05-18.
 */
public class SpringMockMvcException extends RuntimeException {

//**************************************************************************************************
//  Fields
//**************************************************************************************************
  private final HttpStatus httpStatus;

//**************************************************************************************************
//  Constructors
//**************************************************************************************************
  public SpringMockMvcException(HttpStatus httpStatus, String message) {
    super(message);
    this.httpStatus = httpStatus;
  }

//**************************************************************************************************
//  Abstract Methods
//**************************************************************************************************

//**************************************************************************************************
//  Overridden Methods
//**************************************************************************************************

//**************************************************************************************************
//  Other Methods
//**************************************************************************************************

//**************************************************************************************************
//  Getter/Setter Methods
//**************************************************************************************************
  public HttpStatus getHttpStatus() {
    return httpStatus;
  }

//**************************************************************************************************
//  Static Methods
//**************************************************************************************************

//**************************************************************************************************
//  Inner Classes
//**************************************************************************************************

}
